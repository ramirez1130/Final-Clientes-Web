	<section>
		<h3>¿Qué es Apuntec?</h3>
		<p>Apuntec es un desarrollo informatico cuya función primordial es facilitarle a los estudiantes la posibilidad de pedir sus apuntes con anticipación por medio de internet.
		La principal consecuencia de la incorporación del sistema es que modifica la organización del trabajo dentro del local de apuntes.</p>
	</section>
	<section>
		<h3>¿Qué es el local de apuntes?</h3>
		<p>El local de apuntes es donde se retiran los apuntes encargados por los estudiantes mediante el sistema. Tambien ahi pueden sacar fotocopias y cuenta con una libreria que vende utiles de uso comun para los estudiantes.</p>
	</section>
